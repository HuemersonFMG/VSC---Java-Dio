public class OperadoresUnarios {
    public static void main(String[] args) {
        int numero = 5;

        /* incremento */
        System.out.print(numero++);
        System.out.print(numero);

        System.out.print(" ");

        /* Decremento */
        System.out.print(numero--);
        System.out.print(numero);

        System.out.print(" ");

        /* Invers√£o */
        boolean variavel = true;
        System.out.print(!variavel);
    }
}

public class OperadoresTernarios {
    public static void main(String[] args) {
        /*
         * Expressão Boleana - Basicamente substitui o [if] - Retornando uma condição.
         */
        int a, b;
        a = 6;
        b = 6;
        String resultado = a == b ? "Verdadeira" : "Falsa";
        System.out.print("A comparação é: " + resultado);
    }
}
